//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application program which accept amount in US dollar and return its corresponding value in Indian currency. Considering  1$ as 90 rupees
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

int DollarToINR(int iNo)
{
  int iINR = 1;
  int iDollar = 70;

  iINR = iNo * iDollar;
}

int main()
{
  int iValue = 0, iRet = 0;

  printf("Enter number of USD (Input) : ");
  scanf("%d",&iValue);

  iRet = DollarToINR(iValue);

  printf("Value in INR is (Output) : %d\n",iRet);

  return 0;
}
